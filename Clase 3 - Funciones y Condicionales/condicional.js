//CONDICIONAL IF

/*Con el if se evalua una condición 
y se realiza una acción u otra según el 
resultado de dicha evaluación*/

//Estructura
let semaforo = "Verde";

if(semaforo == "Verde"){
    console.log("Avanza")
} else if(semaforo == "Amarillo"){
    console.log("Espera")
} else {
    console.log("Se detiene")
}